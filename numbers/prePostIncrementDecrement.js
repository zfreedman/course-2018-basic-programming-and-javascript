// declare and assign a simple variable
let i = 1;
console.log(`i: ${i}`);

// post increment operator, increment AFTER/POST evaluation
console.log(`new value: ${i++}`);
console.log(`new value: ${i}`);
console.log("--------");

// pre increment operator, increment BEFORE/PRE evaluation
console.log(`new value: ${++i}`);
console.log(`new value: ${i}`);
console.log("--------");

// post decrement operator, decrement AFTER/POST evaluation
console.log(`new value: ${i--}`);
console.log(`new value: ${i}`);
console.log("--------");

// pre decrement operator, decrement BEFORE/PRE evaluation
console.log(`new value: ${--i}`);
console.log(`new value: ${i}`);
console.log("--------");
