// game of basketball
let points = 0;
console.log(`points: ${points}`);

// shooting from on top of a volcano gives 5 points
points = points + 5;
console.log(`points: ${points}`);

// if you fart, you lose 3 points
points = points - 3;
console.log(`points: ${points}`);

// befriend a dinosaur, get 1 point
points += 1;
console.log(`points: ${points}`);

// make dinosaurs mad, you're in trouble (-7 points)
points -= 7;
console.log(`points: ${points}`);
