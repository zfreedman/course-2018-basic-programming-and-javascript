// character access for some character at position n
// can be achieved by referencing position n - 1 using []
console.log("ABCDEFG"[1]);

// length of a string
console.log("length of ABCDEFG:");
console.log("ABCDEFG".length);

// getting the last character of a string
console.log("ABCDEFG"["ABCDEFG".length - 1]);

// empty string
console.log("".length);

// newline character
console.log("several\nline\noutput54");

// tab character
console.log("this\n\tis\n\t\ttabbed");

// string concatenation
console.log("big" + " boy");
